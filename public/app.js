var tour = 1;
function burger_hover() {
  if (tour==1){
      document.getElementById("img_menu").style.transform = "rotate(90deg)";
      document.getElementById("more_div").style.transform = "scaleY(1)";
      document.getElementById("more_div").style.fontSize = "1.7vh";
      document.getElementById("more_div").style.transitionDelay = "0ms, 200ms";
      window["tour"]=0;
  }else{
      document.getElementById("img_menu").style.transform = "rotate(0deg)";
      document.getElementById("more_div").style.transform = "scaleY(0)";
      document.getElementById("more_div").style.fontSize = "0vh";
      document.getElementById("more_div").style.transitionDelay = "200ms, 0ms";
      window["tour"]=1;
  }
}
